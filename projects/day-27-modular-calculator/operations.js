// Day 27 Project: operations.js
// Named exports for basic arithmetic operations

export function add(a, b) {
  return a + b;
}

export function subtract(a, b) {
  return a - b;
}

export function multiply(a, b) {
  return a * b;
}

export function divide(a, b) {
  // TODO (bonus): throw a custom error here if b === 0
  return a / b;
}
